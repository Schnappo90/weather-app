async function getData(city) {
  const url = `https://weather.visualcrossing.com/VisualCrossingWebServices/rest/services/timeline/${city}?key=N9V6Y68ZMC9FN5A59E2FJ2HJ9`;
  try {
    const response = await fetch(url);

    if (response.status == 400) {
      console.log(`Can't retrieve city: ${city}`);
      return;
    }

    const weatherData = await response.json();
    console.log(weatherData);

    if (weatherData == undefined) {
      console.log("Cannot retrieve data");
      return;
    }

    weatherIcon.innerHTML = icons[weatherData.currentConditions.conditions];
    // weatherIcon.innerHTML = icons["Partially cloudy"];
    console.log(icons["Partially cloudy"]);
    cityDisplay.textContent = formatCityName(weatherData.resolvedAddress);
    weatherDescription.textContent = weatherData.currentConditions.conditions;
    temperatureDisplay.textContent =
      weatherData.currentConditions.temp.toFixed(0) + "°";
    humidityDisplay.textContent =
      weatherData.currentConditions.humidity.toFixed(0) + "%";
    windDisplay.textContent = weatherData.currentConditions.windspeed + " mph";
    feelsLikeDisplay.textContent =
      weatherData.currentConditions.feelslike + "°";
  } catch (error) {
    console.log(error);
  }
}

function formatCityName(city) {
  const address = city.split(",")[0];
  return address[0].toUpperCase() + address.slice(1);
}

function convertToCelsius(value) {
  const celsius = ((value - 32) * 5) / 9;
  return celsius.toFixed(1);
}

